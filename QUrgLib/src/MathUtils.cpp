#include "MathUtils.h"
#include <QPoint>

#include <QtGlobal>

#if defined(Q_CC_MSVC)
long lrint(double x)
{
    if (x >= 0) {
        x += 0.5;
    }
    else {
        x -= 0.5;
    }
    return static_cast<long>(x);
}
#endif

/*:::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::*/
/*::  This function converts decimal degrees to radians             :*/
/*:::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::*/
double deg2rad(double deg)
{
    return (deg * M_PI / 180);
}

/*:::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::*/
/*::  This function converts radians to decimal degrees             :*/
/*:::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::*/
double rad2deg(double rad)
{
    return (rad * 180 / M_PI);
}

